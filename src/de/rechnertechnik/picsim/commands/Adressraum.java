package de.rechnertechnik.picsim.commands;

public class Adressraum {

	private Integer from;
	private Integer to;
	
	
	public Adressraum(Integer from, Integer to) {
		this.from = from;
		this.to = to;
	}


	public Integer getFrom() {
		return from;
	}


	public Integer getTo() {
		return to;
	}
	
	
}
